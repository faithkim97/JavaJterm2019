import java.util.*;

/*
To compile:javac  HelloWorldLoop.java
To run: java HelloWorldLoop
*/

public class HelloWorldLoop{
    public static void main(String[] args) {
	//prints Hello World 5 times
	for (int i = 0; i < 5; i++) {
	    System.out.println("Hello World!");
	}

    }//endmain
}//endclass